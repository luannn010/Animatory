// design-sync build input generator (cfg.buildCmd).
//
// Animatory is an app, not a publishable design-system package, so the bundle
// inputs the converter needs don't exist in the repo — this script regenerates
// them, deterministically, from sources already in the repo:
//   • frontend/.design-sync-build/dist/index.mjs  — esbuild of the studio/ui barrel
//       (jsx automatic; react/lucide external) → the converter's runtime --entry
//   • frontend/.design-sync-build/styles.css      — Tailwind compiled against the
//       real tailwind.config.js tokens + src/studio/kit.css → cfg.cssEntry
//   • frontend/.design-sync-build/inter.css + fonts/ — Inter (incl. Vietnamese),
//       vendored from @fontsource-variable/inter, family aliased to the name the
//       components reference → cfg.extraFonts
//   • frontend/build/ts/**.d.ts (+ package.json) — tsc declarations so the
//       converter extracts real prop contracts (the .mjs entry is type-stripped)
//
// Everything it writes is gitignored and regenerated; only this script is
// committed. Depends solely on frontend/node_modules (the app's own install).
// Run from the repo root:  node .design-sync/build/run.mjs

import { createRequire } from 'node:module'
import { execFileSync } from 'node:child_process'
import { cpSync, mkdirSync, readFileSync, rmSync, writeFileSync, readdirSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..', '..')
const fwd = (p) => p.split('\\').join('/')
const FE = join(ROOT, 'frontend')
const NM = join(FE, 'node_modules')
const OUT = join(FE, '.design-sync-build')
const FONTS = join(OUT, 'fonts')
const TS_OUT = join(FE, 'build', 'ts')
const require = createRequire(import.meta.url)

mkdirSync(FONTS, { recursive: true })
mkdirSync(join(OUT, 'dist'), { recursive: true })

// ── 1. Inter: copy the wght-axis woff2 + emit @font-face under BOTH family
// names the components reference ('InterVariable' primary, 'Inter' fallback —
// @fontsource-variable/inter itself registers 'Inter Variable', which matches
// neither, so the brand font would otherwise never load). Derived from the
// package's own wght.css so subset ranges stay correct across font updates.
const interDir = join(NM, '@fontsource-variable', 'inter')
const wght = readFileSync(join(interDir, 'wght.css'), 'utf8')
for (const m of wght.matchAll(/url\(\.\/files\/([^)]+\.woff2)\)/g)) {
  cpSync(join(interDir, 'files', m[1]), join(FONTS, m[1]))
}
const faceFor = (family) =>
  wght.replace(/'Inter Variable'/g, `'${family}'`).replace(/\.\/files\//g, './fonts/')
writeFileSync(
  join(OUT, 'inter.css'),
  `/* GENERATED by .design-sync/build/run.mjs — Inter (wght, incl. Vietnamese),\n` +
    `   aliased to the family names the Animatory components reference. */\n` +
    faceFor('InterVariable') + '\n' + faceFor('Inter') + '\n',
)

// ── 2. Tailwind input: directives + base layer + the real kit.css (read from
// source so the animation/bespoke classes never drift from the app).
const kit = readFileSync(join(FE, 'src', 'studio', 'kit.css'), 'utf8')
writeFileSync(
  join(OUT, '_in.css'),
  `/* GENERATED by .design-sync/build/run.mjs */\n` +
    `@tailwind base;\n@tailwind components;\n@tailwind utilities;\n\n` +
    `@layer base {\n` +
    `  html { font-family: InterVariable, Inter, -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif; -webkit-font-smoothing: antialiased; -moz-osx-font-smoothing: grayscale; }\n` +
    `  body { @apply bg-surface text-ink; margin: 0; }\n` +
    `  * { box-sizing: border-box; }\n` +
    `}\n\n/* ---- ported from src/studio/kit.css ---- */\n${kit}\n`,
)

// ── 3. Tailwind config: real theme + scoped content + a safelist of the
// Animatory semantic tokens (designs consume the static stylesheet only, so the
// agent's on-brand vocabulary must be compiled in even if the app doesn't use
// every token). Paths computed from this file's location → clone-portable.
writeFileSync(
  join(OUT, 'tw.config.mjs'),
  `// GENERATED by .design-sync/build/run.mjs\n` +
    `import { fileURLToPath } from 'node:url'\n` +
    `import { dirname, resolve } from 'node:path'\n` +
    `import base from '../tailwind.config.js'\n` +
    `const root = resolve(dirname(fileURLToPath(import.meta.url)), '..', '..')\n` +
    `const g = (p) => resolve(root, p).split('\\\\').join('/')\n` +
    `const COLORS = ${JSON.stringify(
      'canvas|canvas-dark|surface|surface-soft|surface-code|hairline|hairline-soft|ink|charcoal|slate|steel|stone|muted|paper|on-dark|on-dark-muted|brand-tag|brand-green|brand-green-deep|brand-green-soft|brand-warn|brand-error|stage-rough|stage-bw|stage-color|stage-locked',
    )}\n` +
    `const SPACE = 'xxs|xs|sm|md|lg|xl|xxl|xxxl'\n` +
    `export default {\n` +
    `  ...base,\n` +
    `  content: [g('frontend/src/**/*.{ts,tsx}'), g('.design-sync/previews/**/*.{ts,tsx}')],\n` +
    `  safelist: [\n` +
    `    { pattern: new RegExp('^(?:bg|text|border|ring)-(?:' + COLORS + ')$') },\n` +
    `    { pattern: new RegExp('^(?:gap|gap-x|gap-y|p|px|py|pt|pb|pl|pr|m|mx|my|mt|mb|space-y|space-x)-(?:' + SPACE + ')$') },\n` +
    `    { pattern: /^rounded(?:-(?:xs|sm|md|lg|xl|xxl|full))?$/ },\n` +
    `    { pattern: /^shadow-(?:subtle|card|mockup|brand-tinted)$/ },\n` +
    `    'bg-[#3772cf]', 'text-[#3772cf]', 'border-[#3772cf]', 'ring-[#3772cf]',\n` +
    `    'hover:bg-[#2c5cab]', 'bg-[#3772cf]/10', 'text-[#00b48a]', 'bg-[#00b48a]/10',\n` +
    `  ],\n}\n`,
)

// ── 4. tsconfig for declaration emit (real prop contracts).
writeFileSync(
  join(OUT, 'tsconfig.dts.json'),
  JSON.stringify(
    {
      extends: '../tsconfig.app.json',
      compilerOptions: {
        noEmit: false, declaration: true, emitDeclarationOnly: true,
        outDir: '../build/ts', rootDir: '../src',
        skipLibCheck: true, noUnusedLocals: false, noUnusedParameters: false,
      },
      include: ['../src/studio/ui/**/*.ts', '../src/studio/ui/**/*.tsx', '../src/studio/types.ts'],
    },
    null, 2,
  ) + '\n',
)

// ── 5. Tailwind compile → styles.css
console.log('• tailwind → styles.css')
execFileSync(process.execPath, [
  join(NM, 'tailwindcss', 'lib', 'cli.js'),
  '-c', join(OUT, 'tw.config.mjs'),
  '-i', join(OUT, '_in.css'),
  '-o', join(OUT, 'styles.css'),
  '--minify',
], { stdio: 'inherit', cwd: ROOT })

// ── 6. esbuild the barrel → dist/index.mjs (the converter's runtime entry)
console.log('• esbuild → dist/index.mjs')
const esbuild = require(join(NM, 'esbuild'))
await esbuild.build({
  entryPoints: [fwd(join(FE, 'src/studio/ui/index.ts'))],
  bundle: true, format: 'esm', outfile: fwd(join(OUT, 'dist/index.mjs')),
  jsx: 'automatic',
  external: ['react', 'react-dom', 'react/jsx-runtime', 'react/jsx-dev-runtime', 'lucide-react'],
  nodePaths: [fwd(NM)], logLevel: 'warning',
})

// ── 7. tsc → build/ts, then the package.json that lets the converter resolve
// the barrel .d.ts as the types entry (so inline-typed components extract too).
console.log('• tsc → build/ts')
rmSync(TS_OUT, { recursive: true, force: true })
execFileSync(process.execPath, [
  join(NM, 'typescript', 'lib', 'tsc.js'), '-p', join(OUT, 'tsconfig.dts.json'),
], { stdio: 'inherit', cwd: ROOT })
writeFileSync(join(TS_OUT, 'package.json'), JSON.stringify({ name: '@animatory/ui', types: 'studio/ui/index.d.ts' }, null, 2) + '\n')

// sanity: barrel declarations present
const uiDts = readdirSync(join(TS_OUT, 'studio', 'ui')).filter((f) => f.endsWith('.d.ts'))
console.log(`✓ build inputs ready: ${uiDts.length} .d.ts + styles.css + dist/index.mjs + fonts/`)
